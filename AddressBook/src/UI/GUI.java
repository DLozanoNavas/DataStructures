    public void SaveContact() throws FileNotFoundException, UnsupportedEncodingException {

        Contact CurrentContact = new Contact();
        long phone = Long.parseLong(JtxtPhone.getText());
        String adress = JtxtAdress.getText();
        String name = JtxtName.getText();
        String lastName = JtxtLastName.getText();
        String mail = JtxtMail.getText();

        // Setters declaration
        CurrentContact.setName(name);
        CurrentContact.setLastName(lastName);
        CurrentContact.setPhone(phone);
        CurrentContact.setAdress(adress);
        CurrentContact.setMail(mail);

        boolean exists = false;
        // TODO: 
        if (exists == false) {
            ContactList.add(contactCount, CurrentContact);
            contactCount++;
            CurrentContact.writeFile(ContactList);
        } else {
            JOptionPane.showConfirmDialog(null, "El contacto ya existe.");
        }
        

    }

    public void cleanFields() {

        JtxtName.setText("");
        JtxtLastName.setText("");
        JtxtAdress.setText("");
        JtxtMail.setText("nombre@tu-correo.com");
        JtxtPhone.setText("");

    }

    public int getIndex(long code) {
        Contact StudentIndex = new Contact();
        int query = StudentIndex.searchContact(code, ContactList);
        return query;
    }

    public void generateReport(int index) {
        //JdialogReport.setVisible(true);
        Contact ContactReport = ContactList.get(index);
        String name = ContactReport.getName();
        String lastName = ContactReport.getLastName();
        Long phone = ContactReport.getPhone();
        String adress = ContactReport.getAdress();
        String mail = ContactReport.getMail();

        //Populate TextField
        JtxtAreaReport.setText("Nombre:" + name + "\nApellido: " + lastName + "\nDirección: " + adress + "\nCorreo: " + mail + "\nGénero: ");
    }

