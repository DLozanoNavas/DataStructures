    private void btnSaveStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveStudentActionPerformed
        // TODO add your handling code here:

        String emailRegex = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@" + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        String mail = JtxtMail.getText();

        if (!mail.matches(emailRegex)) {
            JOptionPane.showConfirmDialog(null, "Por favor digite un mail válido.");
        } else if (JtxtPhone.getText().length() != 10) {
            JOptionPane.showConfirmDialog(null, "Por favor digite un teléfono válido de 10 dígitos.");
        } else if (JtxtName.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Por favor digite un nombre.");
        } else if (JtxtLastName.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Por favor digite un apellido.");
        } else if (JtxtAdress.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Por favor digite una dirección.");
        } else {
            // TODO: Save Contact
            cleanFields();
            try{
            SaveContact();
            }catch(Exception e){
                e.getMessage();
            }
        }
    }//GEN-LAST:event_btnSaveStudentActionPerformed

